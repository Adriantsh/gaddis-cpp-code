// Store name, address, phone in string variables
#include <iostream>
#include <string>
using namespace std;

int main()
{
    string name, address, phone;
    name = "Adrian";
    address = "123 Street ave";
    phone = "123456789";

    cout << "Name: " << name << endl;
    cout << "Address: " << address << endl;
    cout << "Phone: " << phone << endl;
    return 0;
}